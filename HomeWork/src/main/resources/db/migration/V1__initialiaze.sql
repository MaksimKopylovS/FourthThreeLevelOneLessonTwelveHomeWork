create table product
(
    id         bigserial primary key,
    title     varchar(255),
    cost      int,
    create_at timestamp default current_timestamp,
    update_at timestamp default current_timestamp
);

create table orders(
    id              bigserial primary key,
    id_product      bigint,
    order_number    bigint,
    count           int,
    sum_cost        int,
    create_at timestamp default current_timestamp
);
